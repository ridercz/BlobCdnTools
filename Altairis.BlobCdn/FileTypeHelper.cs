        public static string GetMimeType(string extension) {
            return GetMimeType(extension, DEFAULT_TYPE);
        }

        public static string GetMimeType(string extension, string defaultType) {
            if (defaultType == null) throw new ArgumentNullException("defaultType");
            if (string.IsNullOrEmpty(defaultType)) throw new ArgumentException("Value cannot be null or empty string.", "defaultType");

            if (string.IsNullOrEmpty(extension)) return defaultType;
            return extensionList[extension] ?? defaultType;
        }

